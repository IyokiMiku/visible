"""题型定义统一规范模块。

所有中文题型名归一化到此表，其他模块不得各自维护映射。
"""

import re
from typing import Any

# ============================================================
# 统一题型规范（唯一事实源）
# ============================================================

# 中文名 → 标准全名（精确全字匹配，所有别名归一化到统一写法）
TYPE_NORMALIZE: dict[str, str] = {
    # 单选题
    "单选题": "单项选择题", "单项选择题": "单项选择题", "单项选择": "单项选择题", "单选": "单项选择题",
    # 多选题
    "多选题": "多项选择题", "多项选择题": "多项选择题", "多项选择": "多项选择题", "多选": "多项选择题",
    # 判断题
    "判断题": "判断题", "判断": "判断题", "是非题": "判断题",
    # 填空题
    "填空题": "填空题", "填空": "填空题",
    # 简答题
    "简答题": "简答题", "简答": "简答题", "问答题": "简答题",
    # 计算题
    "计算题": "计算题", "分析计算题": "计算题", "分析计算": "计算题",
    # 综合题
    "综合题": "综合应用题", "综合应用题": "综合应用题", "综合分析题": "综合应用题", "综合应用": "综合应用题", "综合": "综合应用题",
    # 作图题
    "作图题": "作图题", "作图": "作图题", "画图题": "作图题",
    # 分析题 / 识图题
    "分析题": "分析题", "分析": "分析题",
    "识图题": "识图题", "识图": "识图题",
}

# 标准全名排序（DOCX 章节顺序）
TYPE_ORDER: list[str] = [
    "单项选择题", "多项选择题", "判断题", "填空题", "简答题", "计算题", "综合应用题", "作图题", "分析题", "识图题",
]


def normalize_question_type(type_name: Any, fallback: str = "") -> str:
    """将任意中文题型名归一化为标准全名（精确字典匹配）。"""
    text = re.sub(r"\s+", "", str(type_name or ""))
    return TYPE_NORMALIZE.get(text, fallback or text)


def type_display_name(name: str) -> str:
    """兼容旧接口：normalize_question_type 已直接返回标准全名。"""
    return name
