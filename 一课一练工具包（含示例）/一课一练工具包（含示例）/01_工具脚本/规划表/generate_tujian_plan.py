"""重庆市土建类一课一练考点规划表生成器"""
import openpyxl, os
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side

OUT_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__)))),
                       "04_生成输出", "考点规划表", "重庆市", "土建类")
os.makedirs(OUT_DIR, exist_ok=True)

FONT_TITLE = Font(name="微软雅黑", size=12, bold=True)
FONT_HEADER = Font(name="微软雅黑", size=10, bold=True, color="FFFFFF")
FONT_COURSE = Font(name="微软雅黑", size=10, bold=True)
FONT_SECTION = Font(name="微软雅黑", size=10, bold=True)
FONT_DATA = Font(name="微软雅黑", size=9)
FILL_HEADER = PatternFill(start_color="4472C4", end_color="4472C4", fill_type="solid")
FILL_COURSE = PatternFill(start_color="D6E4F0", end_color="D6E4F0", fill_type="solid")
FILL_VERY_IMP = PatternFill(start_color="FFD7D7", end_color="FFD7D7", fill_type="solid")
FILL_IMP_STD = PatternFill(start_color="FFF2CC", end_color="FFF2CC", fill_type="solid")
BORDER = Border(left=Side("thin"), right=Side("thin"), top=Side("thin"), bottom=Side("thin"))
ALIGN_C = Alignment(horizontal="center", vertical="center", wrap_text=True)
ALIGN_L = Alignment(horizontal="left", vertical="center", wrap_text=True)

QTYPES = "单选5+填空3+综合2"
DIFFICULTY = "75:15:10"
CONFIG = f"题型：{QTYPES} | 难度：{DIFFICULTY}"

def make_xlsx(course_name, sections, filename, textbook):
    wb = openpyxl.Workbook(); ws = wb.active
    for c, w in {"A":6,"B":60,"C":18,"D":8,"E":28,"F":10,"G":6,"H":16}.items():
        ws.column_dimensions[c].width = w
    # R1-3 meta
    ws.merge_cells("A1:H1"); c = ws.cell(row=1,column=1,value="重庆市土建类《一课一练》考点规划表")
    c.font = FONT_TITLE; c.alignment = Alignment(horizontal="center",vertical="center")
    ws.merge_cells("A2:H2"); ws.cell(row=2,column=1,value=CONFIG).font = FONT_DATA
    ws.cell(row=2,column=1).alignment = Alignment(horizontal="center",vertical="center")
    ws.merge_cells("A3:H3"); ws.cell(row=3,column=1,value=textbook).font = FONT_DATA
    ws.cell(row=3,column=1).alignment = Alignment(horizontal="center",vertical="center")
    # R5 headers
    for ci,h in enumerate(["序号","考纲知识点","试卷主题","级别","题型","难度","套数","考纲标号"],1):
        c = ws.cell(row=5,column=ci,value=h); c.font = FONT_HEADER
        c.fill = FILL_HEADER; c.alignment = ALIGN_C
        ws.cell(row=5,column=ci).border = BORDER
    # Course row
    row = 6
    ws.merge_cells(start_row=row,start_column=1,end_row=row,end_column=8)
    ws.cell(row=row,column=1,value=course_name).font = FONT_COURSE
    ws.cell(row=row,column=1).fill = FILL_COURSE
    ws.cell(row=row,column=1).alignment = ALIGN_C; row += 1

    seq = 1
    for sec in sections:
        # Section row (col A empty, col B = section title with indent)
        ws.cell(row=row,column=1,value="").font = FONT_SECTION
        ws.cell(row=row,column=2,value=f"  {sec['title']}").font = FONT_SECTION
        ws.cell(row=row,column=2).alignment = ALIGN_L; row += 1

        for item in sec["items"]:
            num, knowledge, theme, level_raw, exam_ref = item  # tuple
            # 统一级别：掌握→极重要，理解/熟悉→重要，了解→标准
            lv = str(level_raw)
            if lv.startswith("掌握"): level = "极重要"
            elif lv in ("理解","熟悉"): level = "重要"
            elif lv in ("了解","标准"): level = "标准"
            elif lv == "极重要": level = "极重要"
            elif lv == "重要": level = "重要"
            else: level = "标准"
            suffix_list = ["(一)","(二)"] if level == "极重要" else [""]
            for sfx in suffix_list:
                t = f"{theme}{sfx}" if sfx else theme
                ws.cell(row=row,column=1,value=seq).font = FONT_DATA; ws.cell(row=row,column=1).alignment = ALIGN_C
                ws.cell(row=row,column=2,value=knowledge).font = FONT_DATA; ws.cell(row=row,column=2).alignment = ALIGN_L
                ws.cell(row=row,column=3,value=t).font = FONT_DATA; ws.cell(row=row,column=3).alignment = ALIGN_L
                ws.cell(row=row,column=4,value=level).font = FONT_DATA; ws.cell(row=row,column=4).alignment = ALIGN_C
                ws.cell(row=row,column=5,value=QTYPES).font = FONT_DATA; ws.cell(row=row,column=5).alignment = ALIGN_L
                ws.cell(row=row,column=6,value=DIFFICULTY).font = FONT_DATA; ws.cell(row=row,column=6).alignment = ALIGN_C
                ws.cell(row=row,column=7,value=1).font = FONT_DATA; ws.cell(row=row,column=7).alignment = ALIGN_C
                ws.cell(row=row,column=8,value=exam_ref).font = FONT_DATA; ws.cell(row=row,column=8).alignment = ALIGN_L
                fill = FILL_VERY_IMP if level=="极重要" else FILL_IMP_STD
                for ci in range(1,9): ws.cell(row=row,column=ci).fill = fill
                for ci in range(1,9): ws.cell(row=row,column=ci).border = BORDER
                seq += 1; row += 1
    # Borders for header row
    for ci in range(1,9): ws.cell(row=5,column=ci).border = BORDER
    path = os.path.join(OUT_DIR, filename)
    wb.save(path); wb.close()
    return path, seq-1

